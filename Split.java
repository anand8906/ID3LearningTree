
public abstract class Split {
  Integer splitPosition;
  Double  splitEntropy;

  public Integer getSplitPosition() {
	  return splitPosition;
  }

  public void setSplitPosition(Integer splitPosition) {
	  this.splitPosition = splitPosition;
  }

  public Double getSplitEntropy() {
	  return splitEntropy;
  }

  public void setSplitEntropy(Double splitEntropy) {
	  this.splitEntropy = splitEntropy;
  }
  
  
  
}
